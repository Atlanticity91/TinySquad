void TinyRandom::Setup( ) {
	auto seed = time( NULL );
	
	SetSeed( seed );
}

void TinyRandom::SetSeed( const tiny_ulong seed ) { 
	srand( tiny_cast( seed, tiny_uint ) ); 
}
