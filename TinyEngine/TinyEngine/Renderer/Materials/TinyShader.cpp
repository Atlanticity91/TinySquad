TinyShader::TinyShader( )
	: TinyAsset{ TA_TYPE_SHADER },
	TinyGraphicShader{ }
{ }

void TinyShader::Terminate( TinyGame* game ) {
	auto& graphics = game->GetGraphics( );
	auto graphic   = graphics.GetWrapper( );

	TinyGraphicShader::Terminate( graphic );
}
